'''
a simple Python program to calculate the sum of two numbers. I'll include comments to explain each step of the program:
'''

# This is a simple Python program to add two numbers

# Step 1: Define two variables and assign values to them
# Here, we are assigning the value 5 to the variable 'num1'
# and the value 3 to the variable 'num2'
num1 = 5
num2 = 3

# Step 2: Add the two numbers and store the result in a new variable 'sum'
# The '+' operator is used to add the values of 'num1' and 'num2'
sum = num1 + num2

# Step 3: Print the result
# The 'print' function is used to display the value of 'sum' on the screen
print("The sum of", num1, "and", num2, "is", sum)

'''
### Explanation of the Program:

1. **Comments**: 
   - Lines that start with `#` are comments. They are not executed by the program and are used to explain what the code does.

2. **Variable Assignment**: 
   - `num1 = 5`: Assigns the value 5 to the variable `num1`.
   - `num2 = 3`: Assigns the value 3 to the variable `num2`.

3. **Addition Operation**: 
   - `sum = num1 + num2`: Adds the values of `num1` and `num2` and stores the result in the variable `sum`.

4. **Printing the Result**: 
   - `print("The sum of", num1, "and", num2, "is", sum)`: Prints a message that includes the values of `num1`, `num2`, and `sum`. The `print` function is used to display the output.

### Running the Program:
- When you run this program, it will output: `The sum of 5 and 3 is 8`.

This is a very basic example, but it covers the fundamental concepts of variables, arithmetic operations, and printing in Python.



'''
